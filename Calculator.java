
public class Calculator {
	
	public int addTwoInteger(int one, int two) {
		return one + two;
	}
	
	public int subtractTwoInteger(int one, int two) {
		return one - two;
	}
	
	public int multiplyTwoInteger(int one, int two) {
		return one * two;
	}
	 
	public int divideTwoInteger(int one, int two) {
		return one / two;
	}
}
